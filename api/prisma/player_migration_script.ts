async function main() {
  const usedTags = new Set<string>();

  for (const old of oldPlayers) {
    let tag = old.username;

    if (usedTags.has(tag.toLowerCase())) {
      tag = `${tag}_${old.user_id.slice(-4)}`;
      console.log(`⚠️  Tag doublon "${old.username}" → renommé "${tag}"`);
    }
    usedTags.add(tag.toLowerCase());

    await prisma.player.upsert({
      where: { discordId: old.user_id },
      create: {
        name: old.username,
        discordId: old.user_id,
        discordTag: tag,
        elo: old.score,
        wins: old.nb_win,
        losses: old.nb_lose,
        avatarUrl: old.avatar ?? null,
      },
      update: {},
    });

    console.log(`✅ ${old.username} (${old.score} ELO) migré`);
  }

  console.log(`\n🎉 ${oldPlayers.length} joueurs migrés !`);
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect());
